    public partial class CovidVisualization : Form
    {
        LinkedList<DataProcess> allState = null;
        public CovidVisualization()
        {
            InitializeComponent();
            FileReader fr = new FileReader("hello.txt");
            allState = fr.getAllData();
        }


        public DataProcess findState(String s)
        {
            foreach(var item in allState)
            {
                if (item.State.Equals(s))
                {
                    return item;
                }
            }
            return null;
            
        }

        /*
         * call this function when each radious button is clicked
         * to show the data.
         */
        public void showBar(DataProcess state)
        {
            if (state == null)
            {
                Environment.Exit(0);
            }
            foreach (var item in state.AlphaOrder)
            {
                chart1.Series["ethnic"].Points.AddXY(item.Ethnic, item.Data);
            }
        }
        /*
         * auto generated by double-click the button in design page
         */
        private void DC_Button_CheckedChanged(object sender, EventArgs e)
        {
            chart1.Series["ethnic"].Points.Clear();
            DataProcess state = findState("DC");
            showBar(state);
        }

        private void GA_Button_CheckedChanged(object sender, EventArgs e)
        {
